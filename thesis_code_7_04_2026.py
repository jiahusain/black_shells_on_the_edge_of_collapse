@torch.no_grad()
def fno2d_predict(alpha: float, beta: float, zeta0: float,
                  *,
                  H: int = 7,                    # height used in training
                  t_grid=t_grid,                 # (100,) array from file
                  x_mu=x_mean, x_sig=x_std,      # scalars’ training stats
                  y_mu=y_mean.squeeze(),         # (7,)  training mean
                  y_sig=y_std.squeeze(),         # (7,)  training st-dev
                  model=model, device=DEVICE):
    """
    Returns a (7, 100) ndarray with
    [R, V, ρg, ρτ, ρs, T, m] on the original time grid.
    """
    # 1 · normalize the three input scalars exactly as in training
    x_phys   = np.asarray([alpha, beta, zeta0], dtype="float32")   # (3,)
    x_norm   = (x_phys - x_mu) / x_sig                            # (3,)
    # 2 · broadcast over the 7×100 lattice  → shape (1,3,7,100)
    W        = len(t_grid)
    feat     = np.tile(x_norm[:, None, None], (1, H, W))          # (3,7,100)
    x_tensor = torch.tensor(feat[None], dtype=torch.float32,
                            device=device)                        # (1,3,7,100)
    # 3 · forward pass through FNO2d and collapse the dummy height
    y_norm   = model(x_tensor).mean(dim=2).cpu().squeeze(0).numpy()  # (7,100)
    # 4 · de-normalize back to physical units
    y_phys   = y_norm * y_sig[:, None] + y_mu[:, None]             # (7,100)
    return y_phys

