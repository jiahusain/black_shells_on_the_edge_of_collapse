# ---------- stability-test helper: these functions implement the stability tests used in Mathematica -------------------
def stability_flag(state, t, tau_u, tau_axi, slope_eps=1e-8):
    """
    Returns 0 = stable, 1 = unstable  (plus details if required).
    Implements my four Mathematica tests.
    """
    R, *_, m = state
    comp   = m / R
    dt     = np.mean(np.diff(t))

    # derivative with masking
    c_dot  = np.diff(comp, prepend=comp[0]) / dt
    clean  = np.where(np.abs(c_dot) < slope_eps, 0.0, c_dot)

    # --- 1. monotone-slope test ------------------------------------
    win_mono   = 2 * tau_axi
    steps_mono = max(2, int(round(win_mono / dt)))
    signs      = np.sign(clean)
    seg_starts = np.where(np.diff(signs)!=0)[0] + 1
    segs       = np.split(np.column_stack([t, clean]), seg_starts)
    mono_bad   = any(seg[0,1]!=0 and len(seg)>=steps_mono for seg in segs)

    # --- 2. envelope test ------------------------------------------
    win_env    = 2 * tau_u
    steps_env  = max(2, int(round(win_env / dt)))
    half_range = lambda i: 0.5*(comp[max(0,i-steps_env+1):i+1].max() -
                                comp[max(0,i-steps_env+1):i+1].min())
    env_amp    = np.array([half_range(i) for i in range(len(comp))])
    env_bad    = np.any(env_amp > 0.01 * comp[0])

    # --- 3. hi-frequency sign-flip test ----------------------------
    win_flip   = 2 * tau_axi
    steps_flip = max(2, int(round(win_flip / dt)))
    cross_thr  = 4
    flip_counts= [
        np.sum(signs[i:i+steps_flip-1] * signs[i+1:i+steps_flip] < 0)
        for i in range(len(signs)-steps_flip+1)
    ]
    flip_bad   = max(flip_counts) >= cross_thr

    # --- 4. sustained drift test -----------------------------------
    dev_thr    = 0.02 * comp[0]
    win_dev    = 2 * tau_axi
    steps_dev  = max(2, int(round(win_dev / dt)))
    drift_mask = (np.abs(comp - comp[0]) > dev_thr).astype(int)
    drift_sum  = np.convolve(drift_mask, np.ones(steps_dev,int), 'valid')
    drift_bad  = np.any(drift_sum == steps_dev)

    unstable = mono_bad or env_bad or flip_bad or drift_bad
    return int(unstable)
