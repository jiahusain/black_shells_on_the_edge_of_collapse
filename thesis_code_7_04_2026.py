def sobol_qoi(channel="ρg[τ]", stat="max",
              *, N=2000, batch_size=512,
              model_predict=fno2d_predict):
    """
    Compute first- & total-order Sobol' indices for a scalar QoI.
      channel : str or int   – which output variable
      stat    : "max" | "final" | "integral" | callable(traj)->scalar
      N       : base sample count for Saltelli (total ≈ 8 N)
    Returns dicts  S1 , ST  keyed by parameter names.
    """
    # ── resolve channel index
    if isinstance(channel, str):
        channel = var_names.index(channel)
    chan_name = var_names[channel]

    # ── choose how to turn a 100-point curve into one scalar
    if isinstance(stat, str):
        if stat == "max":
            f = lambda y: y[channel].max()
        elif stat == "final":
            f = lambda y: y[channel, -1]
        elif stat == "integral":
            f = lambda y: y[channel].sum()
        else:
            raise ValueError("unknown stat")
    elif callable(stat):
        f = stat
    else:
        raise TypeError("stat must be str or callable")

    # ── SALib problem definition in *normalized* coordinates
    problem = {
        "num_vars": 3,
        "names":    par_names,
        "bounds":   [[-3, 3]]*3          # ±3 σ after normalization
    }

    # ── draw quasi-Monte-Carlo sample
