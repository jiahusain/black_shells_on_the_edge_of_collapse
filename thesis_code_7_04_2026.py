@torch.no_grad()
def _prep_input(alpha, beta, zeta0, *, H=7):
    """Return a (1, 3, 7, 100) tensor with grads enabled on the 3 scalars."""
    x_phys  = np.asarray([alpha, beta, zeta0], dtype="float32")
    x_norm  = (x_phys - x_mean) / x_std                       # (3,)
    feat    = np.tile(x_norm[:, None, None], (1, H, len(t_grid)))  # (3,7,100)
    x_tens  = torch.tensor(feat[None], dtype=torch.float32,
                           device=DEVICE, requires_grad=True)
    return x_tens

def saliency(alpha, beta, zeta0,
             *, collapse=True, functional=lambda y: y[2].max()):
    """
    Gradient d(functional)/d[α,β,ζ₀].
      • functional: scalar of y_hat to differentiate (default: peak ρ_g)
      • collapse  : if True, uses .mean(dim=2) to drop dummy height
    Returns ndarray (3, 7, 100) of gradients.
    """
    model.eval()
    x_tens = _prep_input(alpha, beta, zeta0)
    y_hat  = model(x_tens)
    if collapse:
        y_hat = y_hat.mean(dim=2)      # (1,7,100)
    L = functional(y_hat.squeeze(0))   # scalar
    # -- backward
    model.zero_grad(set_to_none=True)
    L.backward()
    grad = x_tens.grad.detach().cpu().squeeze(0).numpy()  # (3,7,100)
    return grad


