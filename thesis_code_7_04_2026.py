import numpy as np
from functools import partial

_dt = float(t_grid[1] - t_grid[0])          # sampling interval (sec)
_N  = len(t_grid)

def _dominant_bin(sig):
    """Return index of largest magnitude FFT bin (excluding DC)."""
    yf   = np.fft.rfft(sig - sig.mean())    # remove mean for clarity
    idx  = np.argmax(np.abs(yf[1:])) + 1    # +1 to skip DC bin 0
    return idx, yf

def dominant_freq_stat(ch):
    """callable for sobol_qoi → dominant frequency [Hz] of channel ch."""
    def _stat(traj):
        idx, _ = _dominant_bin(traj[ch])
        freqs  = np.fft.rfftfreq(_N, _dt)
        return freqs[idx]
    return _stat

def phase_stat(ch):
    """callable for sobol_qoi → phase (rad) of dominant component."""
    def _stat(traj):
        idx, yf = _dominant_bin(traj[ch])
        return np.angle(yf[idx])
    return _stat


